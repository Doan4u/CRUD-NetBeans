    private void btn_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_deleteActionPerformed
        // TODO add your handling code here:
        int ok=JOptionPane.showConfirmDialog(null, "Apakah Anda Ingin Menghapus Data Ini?","Confirmation",JOptionPane.YES_NO_CANCEL_OPTION);
        if (ok==0){
            try{
                try{
                    String sql="DELETE FROM barang WHERE id_barang='"+txt_idbarang.getText()+"'";
                    PreparedStatement st=con.prepareStatement(sql);
                    st.executeUpdate();
                    JOptionPane.showMessageDialog(null, "Delete Data Berhasil");
                load_table1();
                    reset();
                }catch (Exception e){
                    JOptionPane.showMessageDialog(null, "Delete Data Gagal");
                }
            }catch (Exception e){
                Logger.getLogger(TabelBarang.class.getName()).log(Level.SEVERE, null,e);
            }
        }
    }//GEN-LAST:event_btn_deleteActionPerformed

