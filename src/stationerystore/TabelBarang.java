    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        btn_update.setEnabled(true);
        btn_delete.setEnabled(true);
        btn_simpan.setEnabled(false);
         int baris = jTable1.rowAtPoint(evt.getPoint());
        String id_barang = jTable1.getValueAt(baris, 1).toString();
        txt_idbarang.setText(id_barang);
        String nama_barang = jTable1.getValueAt(baris, 2).toString();
        txt_namabarang.setText(nama_barang);
        String harga = jTable1.getValueAt(baris, 3).toString();
        txt_harga.setText(harga);
        String stok = jTable1.getValueAt(baris, 4).toString();
        txt_stok.setText(stok);
    }//GEN-LAST:event_jTable1MouseClicked

